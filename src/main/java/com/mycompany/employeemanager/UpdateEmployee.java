    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        
        //Get all info from inputs and validate it
        boolean valid = true;

        String name = jTxtName.getText();
        String department = jTxtDepartment.getText();
        
        int id = Integer.parseInt(jTxtID.getText());
        
        int age = 0;
        try {
            age= Integer.parseInt(jTxtAge.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid age, please put a valid input.");
            valid = false;
        }
        
        double salary = 0.00;
        try {
            salary= Double.parseDouble(jTxtSalary.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid salary, please put a valid input.");
            valid = false;
        }
        //If all valid, create new employee with all info
        if(!name.isEmpty()&&!department.isEmpty()&&salary>0&&age>0){
                
          
                Employee employee = new Employee(id,name,age,department,salary);
            //Use updateEmployee method to update the employee    
            try {
                
                db.updateEmployee(employee);
                JOptionPane.showMessageDialog(this, "Employee updated successfully.");  
                jTxtAge.setText("");
                jTxtDepartment.setText("");
                jTxtName.setText("");
                jTxtSalary.setText("");
                
                setUpdateInvisible();
                
            } catch (SQLException ex) {
                Logger.getLogger(AddEmployee.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Error updating the employee.");
            }
     
        }else{
            JOptionPane.showMessageDialog(this, "Invalid input.");
        }
    }//GEN-LAST:event_jBtnUpdateActionPerformed

