    private void jBtnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnLoadActionPerformed
//Get id from input and validate
        boolean valid = true;

        int id = 0;

        try {

            id = Integer.parseInt(jTxtID.getText());

        } catch (Exception e) {
            valid = false;
            JOptionPane.showMessageDialog(this, "ID input invalid, please change it.");
        }
//if valid try find employee if found set all info into each textpane and show update and cancel buttons, load invisible.
        if (valid) {
            try {
                if (db.findEmployee(id) == true) {
                    try {
                        JOptionPane.showMessageDialog(this, "Employee found.");

                        updatingEmployee = db.getEmployee(id);

                        setUpdateVisible();

                        jTxtName.setText(updatingEmployee.getName());
                        jTxtAge.setText("" + updatingEmployee.getAge());
                        jTxtDepartment.setText(updatingEmployee.getDepartment());
                        jTxtSalary.setText("" + updatingEmployee.getSalary());

                        jBtnLoad.setVisible(false);
                        jTxtID.setVisible(false);

                    } catch (SQLException ex) {
                        Logger.getLogger(UpdateEmployee.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Employee not found.");
                }
            } catch (SQLException ex) {
                Logger.getLogger(UpdateEmployee.class.getName()).log(Level.SEVERE, null, ex);

            }
        }

    }//GEN-LAST:event_jBtnLoadActionPerformed
